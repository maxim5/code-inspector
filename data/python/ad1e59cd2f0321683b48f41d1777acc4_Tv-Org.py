def cleanString(string):
	remStrings = [" - ","'","(",")"]
	for r in remStrings:
		string = string.replace(r,"")
	repStrings = [["."," "]]
	for r in repStrings:
		string = string.replace(r[0],r[1])
	return (string.strip())

