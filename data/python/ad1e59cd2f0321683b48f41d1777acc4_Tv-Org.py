def removeEmptyFolders(path):
	if not os.path.isdir(path):return
	files = os.listdir(path)
	if len(files):
		for f in files:
			fullpath = os.path.join(path, f)
			if os.path.isdir(fullpath):removeEmptyFolders(fullpath)
	files = os.listdir(path)
	if len(files) == 0:
		os.rmdir(path)

